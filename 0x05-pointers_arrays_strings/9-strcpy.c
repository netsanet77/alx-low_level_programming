#include "main.h"
#include <string.h>
/**
 * _strcpy - copies the string pointed to by src to buffer pointed to by dest
 * @dest: the pointer that points the destination of the string
 * @src: the pointer that points the source of the string
 * Return: char
 */
char *_strcpy(char *dest, char *src)
{
strcpy(dest, src);
return (dest);
}
